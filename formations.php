					<p>Ces formations suivies par les adultes complètent ou prolongent la scolarité, parfois la remplacent ou du moins en font partie. Il s'agit de rattrapage, de réorientation, de perfectionnement, de recyclage pour les adultes, ou une réponse à leur désir "d'en savoir plus", qu'ils viennent de "sortir de l'école" ou qu'ils soient engagés depuis de nombreuses années dans la vie professionnelle... Les formations proposées dans ce cadre ont une visée essentiellement pratique, soutenue par quelques éléments théoriques.

