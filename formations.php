					<h2 class="h2educ">L'éducation permanente</h2>

					<p>L'éducation permanente se distingue des précédentes par sa conception même de l'Education. Elle considère que l'éducation est constitutive de l'être humain. Il n'y a ni âge ni moment particulier pour se former. Elle considère qu'un être humain vit, évolue, change dans son environnement lui-même en évolution. Elle démonte les mécanismes de ces évolutions et vise à permettre à l'individu d'agir sur son environnement en changement permanent.
					[haut de la page]</p>
					<h3 class="h3educ">Tentative de définition de l'Éducation permanente</h3>
					<h5 class="h5educ">Finalités</h5>

					<p>L'éducation permanente a pour finalité la construction d'une société démocratique d'un point de vue politique, économique, social et culturel, en favorisant l'épanouissement personnel en société et la capacité à agir individuellement et collectivement dans et sur une société en mutation permanente.</p>
					<h5 class="h5educ">Champ</h5>

					<p>L'Education permanente s'inscrit dans un projet de changement de la société en s'appuyant sur une action culturelle, c'est-à-dire symbolique. La culture est une relation symbolique entre l'individu et le monde : elle concerne des intérêts, des représentations, des attitudes ou des valeurs qui renvoient aux conceptions de l'économie, de la société, de la personnalité. Le changement culturel est une modification de cette structure symbolique positive ou négative. Le développement culturel est un changement symbolique jugé positif par un sujet social d'après certains critères du développement de l'économie, de la société ou de la personnalité. L'éducation permanente est une opération de développement culturel.</p>
					<h5 class="h5educ">Public</h5>

					<p>Par ses finalités, l'éducation permanente s'adresse à tous sans distinction d'âge, de sexe, de situation socioprofessionnelle, de lieu d'habitation,... Elle s'adresse autant aux enfants qu'aux adultes, autant aux travailleurs qu'aux chômeurs, autant aux universitaires qu'aux analphabètes, autant aux femmes qu'aux hommes...

					Cependant, les milieux populaires constituent son public privilégié. Ces milieux populaires subissent les inégalités. Par milieu populaire, on entend les travailleurs, avec ou sans emploi, les personnes peu ou pas qualifiées, peu ou pas scolarisées, et les personnes en situation de précarité sociale et/ou économique. La démarche d'Education permanente vise à démonter les mécanismes qui amènent aux inégalités et à agir pour développer une société réellement démocratique, à transformer les rapports économiques et sociaux inégalitaires et à en produire de nouveaux.
					Méthodes

					Promouvoir une société démocratique, c'est déjà avoir une pratique, une méthode la plus démocratique possible. Si les méthodes de l'éducation permanente sont multiples, elles s'inscrivent cependant au sein d'un processus de débats et de participation permanents.

					Par participation, on n'entend pas seulement le fait de permettre à chaque personne en formation de s'exprimer librement, ni uniquement le fait d'alimenter le contenu de la formation avec le vécu des participants, mais bien à permettre aux participants de contrôler le processus de formation : celui-ci n'est pas imposé par le(la) formateur(trice), mais est le résultat d'un débat démocratique entre participants, formateurs et, on l'oublie parfois, l'organisation dans laquelle se passe la formation.

					La promotion d'une société démocratique passe par une démarche elle-même démocratique.
